namespace Mark2;

public class Square
{
    public int X;
    public int Y;
    public int W;
    public int H;
    public int Cx;
    public int Cy;

    public Square(int x, int y, int w, int h, int cx, int cy)
    {
        X = x;
        Y = y;
        W = w;
        H = h;
        Cx = cx;
        Cy = cy;
    }
}
